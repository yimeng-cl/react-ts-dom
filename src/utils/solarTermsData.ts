// 支持的年份范围
export const PRECISE_DATA_YEAR_RANGE = {
  start: 2022,
  end: 2027
};

/**
 * 检查年份是否在精确数据范围内
 */
export function isYearInPreciseRange(year: number): boolean {
  return year >= PRECISE_DATA_YEAR_RANGE.start && year <= PRECISE_DATA_YEAR_RANGE.end;
}

/**
 * 获取指定年份的精确节气数据
 */
export function getPreciseSolarTerms(year: number): SolarTermData[] | null {
  if (!isYearInPreciseRange(year)) {
    return null;
  }
  return PRECISE_SOLAR_TERMS_DATA[year] || null;
}